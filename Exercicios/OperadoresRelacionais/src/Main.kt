fun main() {
    val n1:Int = 5
    val n2:Int = 2

    println("$n1 é naior que $n2? ${n1 > n2}")
    println("$n1 é menor que $n2? ${n1 < n2}")
    println("$n1 maior ou igual a $n2? ${n1 >= n2}")
    println("$n1 menor ou igual a $n2? ${n1 <= n2}")
    println("$n1 é igual a $n2? ${n1 == n2}")
    print("$n1 é deferente de $n2? ${n1 != n2}")
}